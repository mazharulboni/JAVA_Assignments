package com.izanschool.helloworld;

public class Receipe {
    public static void main(String[] args) {

        System.out.println("Name: " + "John Doe");
        ingredients();
        instructions();
    }

    public static void ingredients() {System.out.println("Ingredients: " + "2 cups basmati rice\n" +
            "1 lb boneless, skinless chicken thighs, cut into bite-sized pieces\n" +
            "1 large onion, sliced\n" +
            "3 cloves garlic, minced\n" +
            "1-inch piece of ginger, peeled and minced\n" +
            "2 green chilies, sliced\n" +
            "1/4 cup fresh cilantro, chopped\n" +
            "1/4 cup fresh mint, chopped\n" +
            "1 teaspoon cumin seeds\n" +
            "1 teaspoon coriander seeds\n" +
            "1 teaspoon fennel seeds\n" +
            "4 green cardamom pods\n" +
            "4 whole cloves\n" +
            "1 cinnamon stick\n" +
            "1 bay leaf\n" +
            "1/2 teaspoon turmeric powder\n" +
            "1/2 teaspoon chili powder\n" +
            "Salt to taste\n" +
            "1/4 cup vegetable oil\n" +
            "3 cups water\n" +
            "1/4 cup fried onions (optional)");}
    public static void instructions(){System.out.println("Instructions: " + "Rinse the rice in cold water and soak it in water for 30 minutes. Drain the water and set aside.\n" +
            "In a large pot or Dutch oven, heat the vegetable oil over medium-high heat. Add the cumin seeds, coriander seeds, fennel seeds, green cardamom pods, cloves, cinnamon stick, and bay leaf. Sauté for 1-2 minutes until fragrant.\n" +
            "Add the sliced onions, garlic, ginger, and green chilies. Cook for 5-6 minutes until the onions are translucent.\n" +
            "Add the chicken pieces to the pot and sauté for 5-6 minutes until the chicken is cooked through.\n" +
            "Add the chopped cilantro, mint, turmeric powder, chili powder, and salt to taste. Mix well and cook for another 2-3 minutes.\n" +
            "Add the soaked rice to the pot and mix well to coat the rice with the chicken and spice mixture.\n" +
            "Add the water and bring the mixture to a boil. Reduce the heat to low, cover the pot with a tight-fitting lid, and simmer for 20-25 minutes until the rice is cooked and the liquid is absorbed.\n" +
            "Once the biryani is done, remove it from heat and let it rest for 5 minutes.\n" +
            "Garnish the biryani with fried onions and serve hot with raita (yogurt dip) and salad on the side.");}
}
