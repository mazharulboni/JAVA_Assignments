package com.izanschool.helloworld;

public class JobOpening {
    public static void main(String[] args) {

        System.out.println("Title: " + "Carpenter");
        description();
        requirements();
    }
    public static void description() {
        System.out.println("Description: " + "Reading and interpreting blueprints, building plans, and drawings\n" +
                "Measuring and cutting wood and other building materials using hand and power tools");
    }
    public static void requirements() {
        System.out.println("Requirements : " + "High school diploma or equivalent\n" +
                "Completion of an apprenticeship program or equivalent work experience\n" +
                "Skilled in reading and interpreting blueprints and building plans");
    }
}
